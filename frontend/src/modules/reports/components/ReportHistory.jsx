import React, { useState, useEffect } from "react";
import { FaDownload, FaEye, FaEnvelope, FaSpinner, FaCheckCircle, FaTimesCircle, FaExclamationCircle } from "react-icons/fa";
import reportService from "../services/reportService";
import PreviewModal from "./PreviewModal";
import EmailDialog from "./EmailDialog";

function ReportHistory({ reports, onRefreshHistory }) {
  const [previewOpen, setPreviewOpen] = useState(false);
  const [previewBlobUrl, setPreviewBlobUrl] = useState("");
  const [previewReportName, setPreviewReportName] = useState("");

  const [emailOpen, setEmailOpen] = useState(false);
  const [selectedReportId, setSelectedReportId] = useState(null);
  const [selectedReportName, setSelectedReportName] = useState("");

  const [downloadingId, setDownloadingId] = useState(null);

  useEffect(() => {
    const hasActiveJob = reports.some(r => r.status === "PENDING" || r.status === "IN_PROGRESS");
    if (!hasActiveJob) return;

    const interval = setInterval(() => {
      onRefreshHistory();
    }, 3000);

    return () => clearInterval(interval);
  }, [reports, onRefreshHistory]);

  const handleDownload = async (id, name) => {
    setDownloadingId(id);
    try {
      const blob = await reportService.download(id);
      const url = window.URL.createObjectURL(blob);
      const a = document.createElement("a");
      a.href = url;
      a.download = name.endsWith(".pdf") ? name : `${name}.pdf`;
      document.body.appendChild(a);
      a.click();
      a.remove();
      window.URL.revokeObjectURL(url);
    } catch (error) {
      alert("Failed to download PDF file.");
    } finally {
      setDownloadingId(null);
    }
  };

  const handlePreview = async (id, name) => {
    setPreviewReportName(name);
    setPreviewBlobUrl("");
    setPreviewOpen(true);

    try {
      const blob = await reportService.download(id);
      const url = window.URL.createObjectURL(blob);
      setPreviewBlobUrl(url);
    } catch (error) {
      alert("Failed to retrieve PDF for preview.");
      setPreviewOpen(false);
    }
  };

  const triggerEmail = (id, name) => {
    setSelectedReportId(id);
    setSelectedReportName(name);
    setEmailOpen(true);
  };

  const handleEmailSuccess = (msg) => {
    alert(msg);
  };

  const formatSize = (bytes) => {
    if (!bytes) return "0 KB";
    return (bytes / 1024).toFixed(1) + " KB";
  };

  const renderStatus = (status) => {
    switch (status) {
      case "COMPLETED":
        return (
          <span className="inline-flex items-center gap-1.5 px-3 py-1 rounded-full text-xs font-semibold bg-[#10B981]/10 text-[#10B981] border border-[#10B981]/20">
            <FaCheckCircle /> Completed
          </span>
        );
      case "FAILED":
        return (
          <span className="inline-flex items-center gap-1.5 px-3 py-1 rounded-full text-xs font-semibold bg-[#EF4444]/10 text-[#EF4444] border border-[#EF4444]/20">
            <FaTimesCircle /> Failed
          </span>
        );
      case "IN_PROGRESS":
        return (
          <span className="inline-flex items-center gap-1.5 px-3 py-1 rounded-full text-xs font-semibold bg-[#00D4FF]/10 text-[#00D4FF] border border-[#00D4FF]/20">
            <FaSpinner className="animate-spin" /> Generating
          </span>
        );
      default:
        return (
          <span className="inline-flex items-center gap-1.5 px-3 py-1 rounded-full text-xs font-semibold bg-[#F59E0B]/10 text-[#F59E0B] border border-[#F59E0B]/20">
            <FaExclamationCircle /> Queued
          </span>
        );
    }
  };

  return (
    <div className="bg-[#111B2E] rounded-[24px] border border-white/5 shadow-[0_20px_50px_rgba(0,0,0,0.45)] overflow-hidden animate-fade-in">
      <div className="overflow-x-auto">
        <table className="min-w-full divide-y divide-white/8 text-left text-sm">
          <thead className="bg-[#15223b]/30 text-xs font-bold text-[#94A3B8] uppercase tracking-wider">
            <tr>
              <th className="px-8 py-4">Report Name</th>
              <th className="px-8 py-4">Report Type</th>
              <th className="px-8 py-4">Status</th>
              <th className="px-8 py-4">Generated At</th>
              <th className="px-8 py-4">Generated By</th>
              <th className="px-8 py-4">Size</th>
              <th className="px-8 py-4 text-right">Actions</th>
            </tr>
          </thead>
          <tbody className="divide-y divide-white/8 text-white">
            {reports.map((report) => (
              <tr key={report.id} className="hover:bg-white/[0.02] transition-colors duration-200">
                <td className="px-8 py-5 font-bold text-white">
                  {report.name}
                </td>
                <td className="px-8 py-5 capitalize text-[#94A3B8]">{report.reportType}</td>
                <td className="px-8 py-5">{renderStatus(report.status)}</td>
                <td className="px-8 py-5 text-[#94A3B8]">
                  {new Date(report.generatedTime).toLocaleString()}
                </td>
                <td className="px-8 py-5 text-[#94A3B8]">{report.generatedBy}</td>
                <td className="px-8 py-5 text-[#94A3B8]">{formatSize(report.fileSize)}</td>
                <td className="px-8 py-5 text-right space-x-2">
                  {report.status === "COMPLETED" ? (
                    <>
                      <button
                        onClick={() => handlePreview(report.id, report.name)}
                        title="Preview PDF"
                        className="p-2.5 text-[#94A3B8] hover:text-[#00D4FF] hover:bg-white/5 rounded-xl border border-transparent hover:border-white/5 transition-all duration-200"
                      >
                        <FaEye size={14} />
                      </button>
                      <button
                        onClick={() => handleDownload(report.id, report.name)}
                        title="Download PDF"
                        disabled={downloadingId === report.id}
                        className="p-2.5 text-[#94A3B8] hover:text-[#10B981] hover:bg-white/5 rounded-xl border border-transparent hover:border-white/5 transition-all duration-200 disabled:opacity-50"
                      >
                        {downloadingId === report.id ? (
                          <FaSpinner className="animate-spin" size={14} />
                        ) : (
                          <FaDownload size={14} />
                        )}
                      </button>
                      <button
                        onClick={() => triggerEmail(report.id, report.name)}
                        title="Email PDF"
                        className="p-2.5 text-[#94A3B8] hover:text-[#22D3EE] hover:bg-white/5 rounded-xl border border-transparent hover:border-white/5 transition-all duration-200"
                      >
                        <FaEnvelope size={14} />
                      </button>
                    </>
                  ) : (
                    <span className="text-xs text-[#94A3B8] italic">Processing</span>
                  )}
                </td>
              </tr>
            ))}
            {reports.length === 0 && (
              <tr>
                <td colSpan="7" className="px-8 py-12 text-center text-[#94A3B8]">
                  No compiled reports found in your database history.
                </td>
              </tr>
            )}
          </tbody>
        </table>
      </div>

      <PreviewModal
        isOpen={previewOpen}
        onClose={() => {
          setPreviewOpen(false);
          if (previewBlobUrl) {
            window.URL.revokeObjectURL(previewBlobUrl);
          }
        }}
        pdfBlobUrl={previewBlobUrl}
        reportName={previewReportName}
      />

      <EmailDialog
        isOpen={emailOpen}
        onClose={() => setEmailOpen(false)}
        reportId={selectedReportId}
        reportName={selectedReportName}
        onEmailSent={handleEmailSuccess}
      />
    </div>
  );
}

export default ReportHistory;
